    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
       
        
        String filmName = request.getParameter("film");
        String showTime = request.getParameter("time");
        String[] selectedSeats = request.getParameterValues("seats");

        if (selectedSeats == null || selectedSeats.length == 0) {
            response.getWriter().println("Please select at least one seat.");
            return;
        }

        String seats = String.join(",", selectedSeats);

        Bookings booking = new Bookings();
        booking.setCustomerId(1); // Replace with actual logic
        booking.setFilmName(filmName);
        booking.setShowTime(showTime);
        booking.setSeat(seats);

        try (Connection conn = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/cinematicketsbookingsystem", "root", "1111")) {

            Class.forName("com.mysql.cj.jdbc.Driver"); // Make sure MySQL driver is loaded
            BookingsDAO dao = new BookingsDAO(conn);
            dao.insertBooking(booking);
            response.sendRedirect("customerDashboard.jsp");
        } catch (SQLException | ClassNotFoundException e) {
            throw new ServletException("Database error", e);
        }
    
    
    }
    

